    <section>
        <h2>Data yang Dikumpulkan</h2>
        <ul>
            <li>Data akun pemilik kos, seperti nama, email, nomor WhatsApp, dan password terenkripsi.</li>
            <li>Data kos dan kamar, seperti nama kos, alamat, nomor kamar, fasilitas, harga sewa, status kamar, dan foto kamar.</li>
            <li>Data penghuni yang diinput oleh pemilik kos, seperti nama, nomor identitas, nomor WhatsApp, alamat asal, pekerjaan, kendaraan, kontak darurat, tanggal masuk, dan foto KTP bila diunggah.</li>
            <li>Data pembayaran dan tagihan, seperti nominal, periode tagihan, metode pembayaran, bukti transfer, status verifikasi, dan riwayat transaksi.</li>
            <li>Data perangkat untuk kebutuhan notifikasi, seperti token notifikasi perangkat.</li>
        </ul>
    </section>

    <section>
        <h2>Penggunaan Data</h2>
        <ul>
            <li>Menyediakan fitur pengelolaan kos, kamar, penghuni, tagihan, pembayaran, dan laporan.</li>
            <li>Menampilkan portal penghuni untuk melihat tagihan, informasi kos, metode pembayaran, dan upload bukti pembayaran.</li>
            <li>Mengirim notifikasi terkait tagihan, pembayaran, dan informasi kos.</li>
            <li>Memproses verifikasi pembayaran, saldo QRIS, dan permintaan penarikan dana jika fitur pembayaran otomatis digunakan.</li>
            <li>Menjaga keamanan akun, mencegah penyalahgunaan, dan memperbaiki layanan.</li>
        </ul>
    </section>

